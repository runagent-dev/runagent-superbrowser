"""Emit representative failing Worker tool-call traces from Chinese-model runs.

Walks eval/runs, finds CN-lab runs, classifies each tool attempt (reusing the
analyzer), and pulls 1-2 concrete examples per failure mode: the EXACT tool call
the model emitted + the system's rejection/guard response. Writes
paper/appendix/traces_modelsplit.tex (lstlisting blocks) as direct evidence for
§7.4 — concrete artifacts, not assertions.

Always writes a valid (possibly placeholder) file so \\input never breaks.

Usage:  python -m eval.figures.make_appendix_traces [--runs ...] [--out ...]
"""
from __future__ import annotations

from .. import _bootstrap  # noqa: F401
from .._bootstrap import REPO_ROOT
from .. import analyzer as az
from .. import models as model_registry

import argparse
import json
from pathlib import Path

PAPER = REPO_ROOT.parent / "paper"

# failure class -> human caption (order = presentation order)
TARGETS = [
    ("bad_arg_name", "Tool-schema drift: synonym / missing argument"),
    ("bad_tool_name", "Hallucinated, unregistered tool"),
    ("stale_or_oob_vision_index", r"Out-of-range / stale $[V_n]$ index"),
    ("dead_click_violation", "Dead-click guard violation"),
]
_PER_CLASS = 2


def _clip(s, n=320):
    s = (s or "").replace("\t", " ").strip()
    return s[:n] + (" ..." if len(s) > n else "")


def collect(runs):
    found: dict[str, list[dict]] = {k: [] for k, _ in TARGETS}
    for meta, _seed_dir, workers in runs:
        model_id = (meta.get("model") or {}).get("model", "")
        spec = model_registry.resolve(model_id)
        if not spec or spec.lab != "CN":
            continue
        for w in workers:
            messages = w.get("messages") or []
            registry = az._registry_from_schemas(w.get("tool_schemas"))
            results = {
                m.get("tool_call_id"): az._text_of(m.get("content"))
                for m in messages if m.get("role") == "tool"
            }
            epoch = None
            prev = None
            for m in messages:
                if m.get("role") != "assistant":
                    ep = az._epoch_from_text(az._text_of(m.get("content")))
                    if ep:
                        epoch = ep
                    continue
                for tc in (m.get("tool_calls") or []):
                    fn = tc.get("function") or {}
                    name = fn.get("name") or "(unnamed)"
                    raw = fn.get("arguments")
                    args, parsed = az._parse_args(raw)
                    ok, sub = az._validate_args(name, args if parsed else None, registry)
                    res = results.get(tc.get("id"), "")
                    cls = az._classify_call(name, args if parsed else None, ok, sub, res, epoch, prev)
                    prev = (name, json.dumps(args, sort_keys=True, default=str) if parsed else "?", res[:40])
                    if cls in found and len(found[cls]) < _PER_CLASS:
                        sig = (spec.short_name, name, sub)
                        if any(e["sig"] == sig for e in found[cls]):
                            continue
                        args_str = raw if isinstance(raw, str) else json.dumps(raw, default=str)
                        found[cls].append({
                            "sig": sig, "model": spec.short_name, "name": name,
                            "args": args_str, "result": res,
                        })
    return found


def emit(found) -> str:
    out = [
        r"% Auto-generated by eval/figures/make_appendix_traces.py.",
        r"% Representative Worker-slot failures from Chinese-model runs; evidence for",
        r"% \S\ref{sec:modelfamilies}. Regenerate after eval runs land.",
        "",
    ]
    any_block = False
    for cls, caption in TARGETS:
        items = found.get(cls, [])
        if not items:
            continue
        any_block = True
        out.append(f"\\paragraph{{{caption}.}}")
        for e in items:
            out.append(r"\begin{lstlisting}[basicstyle=\ttfamily\scriptsize,breaklines=true,"
                       r"columns=fullflexible,frame=single,framesep=3pt]")
            out.append(f"[{e['model']}] emitted:")
            out.append(f"  {e['name']}({_clip(e['args'], 280)})")
            out.append("system response:")
            out.append(f"  {_clip(e['result'], 360)}")
            out.append(r"\end{lstlisting}")
        out.append("")
    if not any_block:
        out.append(r"% No Chinese-model failure traces found yet — run the eval first:")
        out.append(r"%   (edit ~/.nanobot/config.json to a CN model) python -m eval.run_eval")
        out.append(r"\emph{(Trace listings will appear here once Chinese-model eval runs are available.)}")
    return "\n".join(out) + "\n"


def main():
    p = argparse.ArgumentParser(description="Emit appendix failure traces")
    p.add_argument("--runs", default=str(REPO_ROOT / "eval" / "runs"))
    p.add_argument("--out", default=str(PAPER / "appendix" / "traces_modelsplit.tex"))
    args = p.parse_args()
    runs = az.load_runs(Path(args.runs))
    found = collect(runs)
    Path(args.out).parent.mkdir(parents=True, exist_ok=True)
    Path(args.out).write_text(emit(found))
    n = sum(len(v) for v in found.values())
    print(f"Wrote {args.out} ({n} trace examples across {len(runs)} runs)")


if __name__ == "__main__":
    main()
